void main() {
    double numeroPI = 3.14159265359;
    // casting primitivo explícito de double para float
    float numeroFloat = (float) numeroPI;
    // casting primitivo explícito de float para int
    int numeroInteiro = (int) numeroFloat;
    IO.println("PI em double: " + numeroPI);
    IO.println("PI em float: " + numeroFloat);
    IO.println("PI em int: " + numeroInteiro);
}


